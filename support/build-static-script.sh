#!/bin/bash
# build-static-script.sh — Build a single-file Jerboa script as a static Linux binary
#
# Usage (inside jerboa21/jerboa Docker container):
#   build-static-script.sh <script.ss> <output-name>
#
# The script compiles <script.ss> with Whole-Program Optimization, embeds
# Chez boot files + the compiled program as C arrays, then links a fully
# static x86_64 binary against the musl Chez kernel and libjerboa_native.a.
#
# Environment variables (all have sane defaults inside the Docker image):
#   JERBOA_HOME              — path to jerboa repo (default: /build/mine/jerboa)
#   JERBOA_MUSL_CHEZ_PREFIX  — musl Chez install prefix (default: /build/chez-musl)
#
# Output: ./<output-name>  (statically linked ELF, target <25 MB)
#
# See ~/mine/jerboa/docs/single-binary.md for architecture details.

set -euo pipefail

SCRIPT="${1:?Usage: build-static-script.sh <script.ss> <output-name>}"
OUTPUT="${2:?Usage: build-static-script.sh <script.ss> <output-name>}"

JERBOA_HOME="${JERBOA_HOME:-/build/mine/jerboa}"
MUSL_CHEZ="${JERBOA_MUSL_CHEZ_PREFIX:-/build/chez-musl}"
NATIVE_A="${JERBOA_HOME}/jerboa-native-rs/target/x86_64-unknown-linux-musl/release/libjerboa_native.a"
LIBDIRS="${JERBOA_HOME}/lib"
SCHEME="${SCHEME:-scheme}"

echo "=== Jerboa static build: ${SCRIPT} → ${OUTPUT} ==="
echo "    JERBOA_HOME = ${JERBOA_HOME}"
echo "    MUSL_CHEZ   = ${MUSL_CHEZ}"
echo ""

# ── Locate musl Chez libkernel.a ─────────────────────────────────────────────
MACHINE_TYPE=$(${SCHEME} -q <<'EOF'
(display (machine-type)) (exit)
EOF
)

CSV_DIR=""
for d in "${MUSL_CHEZ}/lib/csv"*/"${MACHINE_TYPE}"; do
    if [ -f "${d}/libkernel.a" ]; then
        CSV_DIR="${d}"
        break
    fi
done

if [ -z "${CSV_DIR}" ] || [ ! -f "${CSV_DIR}/libkernel.a" ]; then
    echo "ERROR: Cannot find musl libkernel.a under ${MUSL_CHEZ}/lib/csv*/${MACHINE_TYPE}" >&2
    echo "  machine-type reported: ${MACHINE_TYPE}" >&2
    exit 1
fi

echo "  Chez:   ${CSV_DIR}"
echo "  Native: ${NATIVE_A}"
echo ""

if [ ! -f "${NATIVE_A}" ]; then
    echo "ERROR: ${NATIVE_A} not found" >&2
    echo "  Run: cd ${JERBOA_HOME}/jerboa-native-rs && cargo build --release --target x86_64-unknown-linux-musl --no-default-features" >&2
    exit 1
fi

# ── Step 1: Compile script with WPO → <output>.wp.so ─────────────────────────
WPO_SO="${OUTPUT}.wp.so"
# Writable dir for compiled library .so output (source lib dir may be read-only)
OBJ_DIR="/tmp/jerboa-obj-$$"
mkdir -p "${OBJ_DIR}"
echo "==> Step 1: Compile ${SCRIPT} with WPO → ${WPO_SO}"
echo "    Library object cache: ${OBJ_DIR}"

LD_LIBRARY_PATH="${JERBOA_HOME}/jerboa-native-rs/target/release:${LD_LIBRARY_PATH:-}" \
  ${SCHEME} --libdirs "${LIBDIRS}" \
    --script "${JERBOA_HOME}/support/build-boot.ss" "${SCRIPT}" "${WPO_SO}" "${OBJ_DIR}"

echo ""

# ── Step 2: Convert boot files and program .so to C byte-array headers ────────
echo "==> Step 2: Convert to C headers"

convert_to_header() {
    local input="$1"
    local stem="$2"
    local varname
    varname="$(basename "${stem}")_data"
    local sizename
    sizename="$(basename "${stem}")_size"
    local header="${stem}.h"

    echo "  ${input} → ${header}"
    printf "static const unsigned char %s[] = {\n" "${varname}" > "${header}"
    od -An -tx1 -v "${input}" \
        | sed 's/^ *//;s/ *$//;s/  */ /g;s/ /,0x/g;s/^/0x/;s/$/,/' >> "${header}"
    printf "};\n" >> "${header}"
    printf "static const unsigned int %s = sizeof(%s);\n" \
        "${sizename}" "${varname}" >> "${header}"
}

convert_to_header "${CSV_DIR}/petite.boot" "petite_boot"
convert_to_header "${CSV_DIR}/scheme.boot" "scheme_boot"
convert_to_header "${WPO_SO}"             "program_boot"

echo ""

