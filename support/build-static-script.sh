/* ── main ─────────────────────────────────────────────────────────────────── */
/*
 * Write embedded .so to a mkstemp file so Sscheme_program can load it.
 * memfd_create is ideal but requires Linux 3.17+ — use mkstemp for
 * broader compatibility.
 */
static const char *write_program_tmpfile(void) {
    static char path[] = "/tmp/jerboa-prog-XXXXXX";
    int fd = mkstemp(path);
    if (fd < 0) { perror("mkstemp"); exit(1); }
    ssize_t n = write(fd, program_boot_data, program_boot_size);
    if (n != (ssize_t)program_boot_size) { perror("write"); close(fd); unlink(path); exit(1); }
    close(fd);
    return path;
}

int main(int argc, const char *argv[]) {
    /* Signal stdlib modules to skip (load-shared-object ...) calls */
    putenv("JERBOA_STATIC=1");
    Sscheme_init(NULL);
    Sregister_boot_file_bytes("petite", (void *)petite_boot_data, petite_boot_size);
    Sregister_boot_file_bytes("scheme", (void *)scheme_boot_data, scheme_boot_size);
    Sbuild_heap(NULL, custom_init);

    const char *prog_path = write_program_tmpfile();
    Sscheme_program(prog_path, argc, argv);
    unlink(prog_path);

    Sscheme_deinit();
    return 0;
}
